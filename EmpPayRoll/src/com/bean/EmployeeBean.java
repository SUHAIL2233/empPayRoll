package com.bean;

public class EmployeeBean {
	private int empId;
	private String empName;
	private String empDOB;
	private String empDesignation;
	private double empSalary;
	private long empMobileNumber;
	private String empEmail;
	private String empLocation;
	/**
	 * @return the empId
	 */
	public int getEmpId() {
		return empId;
	}
	/**
	 * @param empId the empId to set
	 */
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	/**
	 * @return the empName
	 */
	public String getEmpName() {
		return empName;
	}
	/**
	 * @param empName the empName to set
	 */
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	/**
	 * @return the empDOB
	 */
	public String getEmpDOB() {
		return empDOB;
	}
	/**
	 * @param empDOB the empDOB to set
	 */
	public void setEmpDOB(String empDOB) {
		this.empDOB = empDOB;
	}
	/**
	 * @return the empDesignation
	 */
	public String getEmpDesignation() {
		return empDesignation;
	}
	/**
	 * @param empDesignation the empDesignation to set
	 */
	public void setEmpDesignation(String empDesignation) {
		this.empDesignation = empDesignation;
	}
	/**
	 * @return the empSalary
	 */
	public double getEmpSalary() {
		return empSalary;
	}
	/**
	 * @param empSalary the empSalary to set
	 */
	public void setEmpSalary(double empSalary) {
		this.empSalary = empSalary;
	}
	/**
	 * @return the empMobileNumber
	 */
	public long getEmpMobileNumber() {
		return empMobileNumber;
	}
	/**
	 * @param empMobileNumber the empMobileNumber to set
	 */
	public void setEmpMobileNumber(long empMobileNumber) {
		this.empMobileNumber = empMobileNumber;
	}
	/**
	 * @return the empEmail
	 */
	public String getEmpEmail() {
		return empEmail;
	}
	/**
	 * @param empEmail the empEmail to set
	 */
	public void setEmpEmail(String empEmail) {
		this.empEmail = empEmail;
	}
	/**
	 * @return the empLocation
	 */
	public String getEmpLocation() {
		return empLocation;
	}
	/**
	 * @param empLocation the empLocation to set
	 */
	public void setEmpLocation(String empLocation) {
		this.empLocation = empLocation;
	}
	
	
	
}
